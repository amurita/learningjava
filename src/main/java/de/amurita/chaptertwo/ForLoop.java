package de.amurita.chaptertwo;

// Class to show the functionality of a for-loop.
public class ForLoop {

    // Counting up, in steps of 1, from 1 to 10.
    public static void main ( String [] args ) {
        for ( int i = 1; i <= 10; i++ ) // i is a loop counter
            System.out.println( i );
    }
}