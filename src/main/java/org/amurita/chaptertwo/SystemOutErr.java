package org.amurita.chaptertwo;

// Class to show how to print to std.-output/-error.
public class SystemOutErr {

    // Print std output and - error messages.
    public static void main( String[] args ) {
        System.out.println( "This line uses standard output." ); // Standard output.
        System.err.println( "This line uses standard error output." ); // Standard Error output.
    }
}
